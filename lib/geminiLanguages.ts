export function geminiLanguageLabel(code: string): string {
  const c = code.trim();
  return GEMINI_LANGUAGES.find((l) => l.code === c)?.label ?? c.toUpperCase();
}

/** Normalize picker codes for Gemini API (e.g. legacy `zh` → `zh-Hans`). */
export function normalizeGeminiLanguageCode(code: string): string {
  const c = code.trim().toLowerCase();
  if (c === 'zh') return 'zh-Hans';
  if (c === 'pt') return 'pt-BR';
  if (c === 'tl') return 'fil';
  if (c === 'iw') return 'he';
  return code.trim();
}
